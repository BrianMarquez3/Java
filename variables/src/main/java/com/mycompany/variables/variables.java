public class variables {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // byte - 8 bits
        // short - 16 bit
        // int 32 bits
        // long 64 bits
        // float 32 bits
        // double 64 bits
        // char 16 bits
        // bollean - i bits
        
        byte mordida;
        mordida = 126;
        System.out.println("Mordida="+ mordida);
        
        short corto;
        corto = 32000;
        System.out.println("Corto="+ corto);
        
        int entero;
        entero = 33000;
        System.out.println("Entero="+ entero);
        
        
        
    }
    
}
